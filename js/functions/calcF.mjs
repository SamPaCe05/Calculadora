function sumatoria(a = 0, b = 0) {
    return a + b
}
function restar(a = 0, b = 0) {
    return a - b
}
function mult(a = 0, b = 0) {
    return a * b
}
function div(a = 0, b = 0) {
    return a / b
}
function porcentaje(a = 0, b = 0) {
    return a / 100 * b
}
export { sumatoria, restar, mult, div, porcentaje }